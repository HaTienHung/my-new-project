import { useState, useEffect, useRef } from "react";
import { FaUser, FaEnvelope, FaPhone, FaMapMarkerAlt } from "react-icons/fa";
import { toast } from "react-toastify";



export default function AuthModal() {
  const [isOpen, setIsOpen] = useState(false);
  const [isLogin, setIsLogin] = useState(true);
  const [isLoggedIn, setIsLoggedIn] = useState(false);
  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [password_confirmation, setPasswordConfirmation] = useState("");
  const [name, setName] = useState("");
  const [phone_number, setPhoneNumber] = useState("");
  const [address, setAddress] = useState("");
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState("");
  const [user, setUser] = useState<{ name: string, phone_number: number, address: string, email: string } | null>(null);
  const emailRef = useRef<HTMLInputElement | null>(null);

  // T·∫£i th√¥ng tin user khi trang load l·∫°i
  useEffect(() => {
    const storedUser = localStorage.getItem("user");
    const token = localStorage.getItem("token");

    if (storedUser && token) {
      setUser(JSON.parse(storedUser));
      setIsLoggedIn(true);
    }
  }, []);


  useEffect(() => {
    if (isOpen) {
      emailRef.current?.focus(); // T·ª± ƒë·ªông focus v√†o email khi m·ªü modal
    }
  }, [isOpen]);


  // H√†m x·ª≠ l√Ω ƒëƒÉng nh·∫≠p
  const handleLogin = async () => {
    setLoading(true);
    setError("");

    try {
      const response = await fetch(`${process.env.NEXT_PUBLIC_API_URL}/app/users/login`, {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({ email, password }),
      });

      const data = await response.json();
      // console.log(data);

      if (!response.ok) {
        throw new Error(data.message || "ƒêƒÉng nh·∫≠p th·∫•t b·∫°i!");
      }

      // üõ† L∆∞u token & user v√†o localStorage
      localStorage.setItem("token", data.data.token);
      localStorage.setItem("user", JSON.stringify(data.data.user));

      // C·∫≠p nh·∫≠t tr·∫°ng th√°i ƒëƒÉng nh·∫≠p
      setUser(data?.data?.user ?? null);
      setIsLoggedIn(true);
      setIsOpen(true); // ƒê√≥ng modal sau khi ƒëƒÉng nh·∫≠p th√†nh c√¥ng
      toast.success("ƒêƒÉng nh·∫≠p th√†nh c√¥ng");
    } catch (err) {
      const errorMessage = err instanceof Error ? err.message : "ƒê√£ x·∫£y ra l·ªói!";
      setError(errorMessage);
    } finally {
      setLoading(false);
    }
  };
  const handleLogout = () => {
    setIsLoggedIn(false);
    setUser(null);
    localStorage.removeItem("token");
    localStorage.removeItem("user");
    toast.success("ƒêƒÉng xu·∫•t th√†nh c√¥ng");
  };

  const handleRegister = async () => {
    setLoading(true);
    setError("");

    try {
      const response = await fetch(`${process.env.NEXT_PUBLIC_API_URL}/app/users/register`, {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          name,
          phone_number,
          address,
          email,
          password,
          password_confirmation
        }),
      });

      const data = await response.json();

      if (!response.ok) {
        throw new Error(data.message || "ƒêƒÉng k√Ω th·∫•t b·∫°i!");
      }

      toast.success("ƒêƒÉng k√Ω th√†nh c√¥ng, vui l√≤ng ƒëƒÉng nh·∫≠p");
      setIsLogin(true); // Chuy·ªÉn v·ªÅ form ƒëƒÉng nh·∫≠p sau khi ƒëƒÉng k√Ω th√†nh c√¥ng
    } catch (err) {
      setError(err instanceof Error ? err.message : "C√≥ l·ªói x·∫£y ra!");
    } finally {
      setLoading(false);
    }
  };

  return (
    <>
      {/* Button m·ªü modal */}
      <button
        className="flex items-center px-4 text-lg"
        onClick={() => setIsOpen(true)}
      >
        <FaUser className="hover:scale-110 transition-transform duration-300" />
        <span className="hidden md:hidden lg:inline text-base ml-1 hover:underline underline-offset-4">
          T√†i kho·∫£n
        </span>
      </button>

      {/* Modal */}
      {isOpen && (
        <div
          className="fixed inset-0 flex items-center justify-center z-50"
          onMouseDown={(e) => {
            if (e.target === e.currentTarget) {
              setTimeout(() => setIsOpen(false), 100); // ƒê·ª£i 100ms ƒë·ªÉ tr√°nh ƒë√≥ng modal khi blur
            }
          }}
        >
          <div
            className="bg-white p-10 rounded-lg shadow-lg w-[450px] relative z-50"
            onClick={(e) => e.stopPropagation()}
          >
            {isLoggedIn ? (
              <>
                <h2 className="text-xl font-semibold mb-4 text-center">Th√¥ng tin t√†i kho·∫£n</h2>
                <div className="p-4 bg-gray-100 rounded-lg">
                  {/* ·∫¢nh ƒë·∫°i di·ªán (n·∫øu c√≥) */}
                  <div className="flex justify-center mb-4">
                    <FaUser className="text-6xl text-gray-500" />
                  </div>

                  {/* T√™n ng∆∞·ªùi d√πng */}
                  <p className="text-lg font-semibold text-center">{user?.name ?? "Ng∆∞·ªùi d√πng"}</p>

                  {/* Danh s√°ch th√¥ng tin */}
                  <div className="mt-4 space-y-3 text-sm">
                    <div className="flex items-center space-x-2">
                      <FaEnvelope className="text-gray-500" />
                      <span>{user?.email ?? "Ch∆∞a c·∫≠p nh·∫≠t"}</span>
                    </div>

                    <div className="flex items-center space-x-2">
                      <FaPhone className="text-gray-500" />
                      <span>{user?.phone_number ?? "Ch∆∞a c·∫≠p nh·∫≠t"}</span>
                    </div>

                    <div className="flex items-center space-x-2">
                      <FaMapMarkerAlt className="text-gray-500" />
                      <span>{user?.address ?? "Ch∆∞a c·∫≠p nh·∫≠t"}</span>
                    </div>
                  </div>
                </div>
                <button
                  className="w-full bg-red-500 text-white p-2 rounded"
                  onClick={() => {
                    setIsLoggedIn(false);
                    setIsLogin(true);
                    localStorage.removeItem("token"); // X√≥a token khi logout
                    setIsOpen(true);
                    handleLogout()
                  }}
                >
                  ƒêƒÉng xu·∫•t
                </button>
              </>
            ) : (
              <>
                <h2 className="text-xl font-semibold mb-4 text-center">
                  {isLogin ? "ƒêƒÉng nh·∫≠p" : "ƒêƒÉng k√Ω"}
                </h2>

                {isLogin ? (
                  <>
                    <form onSubmit={(e) => {
                      e.preventDefault(); // NgƒÉn form reload trang
                      handleLogin(); // G·ªçi h√†m x·ª≠ l√Ω ƒëƒÉng nh·∫≠p
                    }}>
                      <input
                        ref={emailRef} // G√°n ref v√†o input
                        type="email"
                        placeholder="Email"
                        value={email}
                        onChange={(e) => setEmail(e.target.value)}
                        className="w-full p-2 border border-gray-300 rounded mb-3 focus:ring-2 focus:ring-[rgb(121,100,73)] outline-none"
                      />
                      <input
                        type="password"
                        placeholder="M·∫≠t kh·∫©u"
                        value={password}
                        onChange={(e) => setPassword(e.target.value)}
                        className="w-full p-2 border border-gray-300 rounded mb-3 focus:ring-2 focus:ring-[rgb(121,100,73)] outline-none"
                      />

                      {error && <p className="text-red-500 text-sm mb-2">{error}</p>}

                      <button
                        className="w-full bg-blue-500 text-white p-2 rounded"
                        type="submit"
                        disabled={loading}
                      >
                        {loading ? "ƒêang ƒëƒÉng nh·∫≠p..." : "ƒêƒÉng nh·∫≠p"}
                      </button>
                      <p className="text-center mt-2 text-sm">
                        Ch∆∞a c√≥ t√†i kho·∫£n?{" "}
                        <span
                          className="text-blue-500 cursor-pointer"
                          onClick={() => setIsLogin(false)}
                        >
                          ƒêƒÉng k√Ω ngay
                        </span>
                      </p>
                    </form>
                  </>
                ) : (
                  <>
                    <input
                      type="text"
                      placeholder="T√™n"
                      value={name}
                      onChange={(e) => setName(e.target.value)}
                      className="w-full p-2 border border-gray-300 rounded mb-3 focus:ring-2 focus:ring-[rgb(121,100,73)] outline-none"
                    />
                    <input
                      type="text"
                      placeholder="S·ªë ƒëi·ªán tho·∫°i"
                      value={phone_number}
                      onChange={(e) => setPhoneNumber(e.target.value)}
                      className="w-full p-2 border border-gray-300 rounded mb-3 focus:ring-2 focus:ring-[rgb(121,100,73)] outline-none"
                    />
                    <input
                      type="text"
                      placeholder="ƒê·ªãa ch·ªâ"
                      value={address}
                      onChange={(e) => setAddress(e.target.value)}
                      className="w-full p-2 border border-gray-300 rounded mb-3 focus:ring-2 focus:ring-[rgb(121,100,73)] outline-none"
                    />
                    <input
                      type="text"
                      placeholder="Email"
                      value={email}
                      onChange={(e) => setEmail(e.target.value)}
                      className="w-full p-2 border border-gray-300 rounded mb-3 focus:ring-2 focus:ring-[rgb(121,100,73)] outline-none"
                    />
                    <input
                      type="password"
                      placeholder="M·∫≠t kh·∫©u"
                      value={password}
                      onChange={(e) => setPassword(e.target.value)}
                      className="w-full p-2 border border-gray-300 rounded mb-3 focus:ring-2 focus:ring-[rgb(121,100,73)] outline-none"
                    />
                    <input
                      type="password"
                      placeholder="Nh·∫≠p l·∫°i m·∫≠t kh·∫©u"
                      value={password_confirmation}
                      onChange={(e) => setPasswordConfirmation(e.target.value)}
                      className="w-full p-2 border border-gray-300 rounded mb-3 focus:ring-2 focus:ring-[rgb(121,100,73)] outline-none"
                    />

                    {error && <p className="text-red-500 text-sm mb-2">{error}</p>}
                    <button
                      className="w-full bg-green-500 text-white p-2 rounded"
                      onClick={handleRegister}
                      disabled={loading}
                    >
                      {loading ? "ƒêang ƒëƒÉng k√Ω..." : "ƒêƒÉng k√Ω"}
                    </button>
                    <p className="text-center mt-2 text-sm">
                      ƒê√£ c√≥ t√†i kho·∫£n?{" "}
                      <span
                        className="text-blue-500 cursor-pointer"
                        onClick={() => setIsLogin(true)}
                      >
                        ƒêƒÉng nh·∫≠p ngay
                      </span>
                    </p>
                  </>
                )}
              </>
            )}

            <button
              className="w-full mt-3 bg-gray-300 p-2 rounded"
              onClick={() => setIsOpen(false)}
            >
              ƒê√≥ng
            </button>
          </div>
        </div>
      )}
    </>
  );
}
